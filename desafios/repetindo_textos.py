# Desafio 2: Repetindo Textos
# Descrição: Solicitar uma string e um número inteiro como entrada para retornar a string repetida.

texto = input("Digite o texto que deseja repetir: ")
vezes = int(input("Digite a quantidade de vezes: "))

# O GitHub Copilot sugere o uso do operador * para repetição de strings em Python
print((texto + " ") * vezes)
